#this class defines the functionality of the sic codes
class SIC

	#return an array of all 4 digit sics (as strings) that are children of the given root
	#the given root can be a 1-digit, 2-digit, or 3-digit
	def self.get_sics(root)
		len = root.length
		sics = SIC.hierarchy

		result = Array.new
		started = false
		done = false

		for sic in sics
			done = true if started and sic.length == len
			next if done
			started = true if sic == root
			next unless started

			if sic.length == 4
				result << sic
			end
		end

		result
	end

	#return an array containing the immediate children of the given sic root
	#root can be a 1-digit, 2-digit, or 3-digit
	#if 1-digit is given, only 2-digits are returned; if 2-digit is given, only 3-digits are returned etc.
	#hash should be in a SIC -> Description order (both strings)
	def self.get_children(root)
		len = root.length
		sics = SIC.hierarchy

		result = Array.new
		started = false
		done = false

		for sic in sics
			done = true if started and sic.length == len
			next if done
			started = true if sic == root
			next unless started

			if sic.length == len+1
				result << sic
			end
		end

		SIC.add_description(result)
	end

	#returns the most recent SIC parent of the given child
	def self.get_parent(child)
		len = child.length
		if len <= 1
			return child
		end

		sics = SIC.hierarchy
		parent = ""

		for sic in sics
			if sic.length+1 == len
				parent = sic
			end
			if sic == child
				return parent
			end
		end

		return child
	end

	#takes in an array of SICs and returns a 2-dimentional array now with the descriptions
	def self.add_description(array)
		unless array.kind_of?(Array)
			desc = array + " - " + SIC.description(array)
			# if desc.length > 19
			# 	desc = desc.slice(0,19) + ".."
			# end
			return [desc]
		end
		result = Array.new
		result << ["Select to Narrow Search",""]
		for sic in array
			desc = sic + " - " + SIC.description(sic)
			# if desc.length > 19
			# 	desc = desc.slice(0,19) + ".."
			# end
			result << [desc, sic]
		end

		result
	end

